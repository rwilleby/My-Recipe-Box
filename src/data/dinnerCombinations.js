export function getDinnerCombinationSearchText(meal) {
  return [
    meal.title,
    meal.subtitle,
    meal.mainDish,
    meal.mainServing,
    ...(meal.sides || []).flatMap((side) => [side.name, side.serving]),
    ...(meal.tags || []),
  ]
    .filter(Boolean)
    .join(" ")
    .toLowerCase();
}
