function codePrefix(id = "") { return id.match(/^[A-Z]+/)?.[0] || ""; }
function defaultCost(price = "$$") { const base = price === "$" ? 10 : price === "$$$" ? 28 : 18; return { 2: +(base*0.55).toFixed(2), 4: +base.toFixed(2), 6: +(base*1.45).toFixed(2) }; }
